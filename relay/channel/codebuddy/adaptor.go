// mapModelName 处理特定模型名称的映射或转换
// 某些模型名称可能需要特殊处理以避免服务器端路由问题
func mapModelName(modelName string) string {
	// 如果模型名称包含 "codex"，可能需要特殊处理
	// 根据实际情况调整映射规则
	switch {
	case strings.Contains(modelName, "gpt-5-codex"):
		// 尝试使用不同的模型名称，或者保持原样
		// 这里可以根据 CodeBuddy 服务器的实际要求调整
		return modelName
	default:
		return modelName
	}
}

func (a *Adaptor) Init(info *relaycommon.RelayInfo) {
}

func (a *Adaptor) GetRequestURL(info *relaycommon.RelayInfo) (string, error) {
	// Use v2 endpoint instead of v1
	return fmt.Sprintf("%s/v2/chat/completions", info.ChannelBaseUrl), nil
}

func (a *Adaptor) SetupRequestHeader(c *gin.Context, req *http.Header, info *relaycommon.RelayInfo) error {
	channel.SetupApiRequestHeader(info, c, req)
	req.Set("Authorization", "Bearer "+info.ApiKey)
	// Custom headers are automatically applied via HeaderOverride in api_request.go
	return nil
}

func (a *Adaptor) ConvertOpenAIRequest(c *gin.Context, info *relaycommon.RelayInfo, request *dto.GeneralOpenAIRequest) (any, error) {
	if request == nil {
		return nil, errors.New("request is nil")
	}

	// 应用模型名称映射
	request.Model = mapModelName(request.Model)

	// Force stream mode - CodeBuddy only supports streaming
	request.Stream = true
	info.IsStream = true
	return request, nil
}

